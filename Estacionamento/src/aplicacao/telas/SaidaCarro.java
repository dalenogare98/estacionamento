    private void pagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagarActionPerformed
         if(interfaceControladorCarro.jaPossuiCarro(textPlaca.getText())){
             this.interfaceControladorCarro.removerCarro(textPlaca.getText());
             this.setVisible(false);
             this.owner.setVisible(true);
             horaDeEntrada.setText("");
             textPlaca.setText("");
             valorAPagar.setText("");
             horas.setValue(6);
             minutos.setValue(0);
         }
         else{
             JOptionPane.showMessageDialog(null, " Placa não registrada no sistema ");
         }
    }//GEN-LAST:event_pagarActionPerformed

