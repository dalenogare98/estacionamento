    private void textPlacaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textPlacaKeyReleased
        if(interfaceControladorCarro.jaPossuiCarro(textPlaca.getText())){
            horaDeEntrada.setText(interfaceControladorCarro.getHoraDeEntradaEmHoras(textPlaca.getText()));
            Integer temp = interfaceControladorCarro.getHoraDeEntrada((textPlaca.getText()));
        Integer temp2 = 0;
        while(temp >= 60){
            temp2++;
            temp = temp - 60;
        }
        horas.setValue(new Integer(temp2));
        minutos.setValue(new Integer(temp));
        }
        else{
            horaDeEntrada.setText("");
        }
    }//GEN-LAST:event_textPlacaKeyReleased

