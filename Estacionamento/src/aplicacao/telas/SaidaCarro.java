    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
        if(interfaceControladorCarro.jaPossuiCarro(textPlaca.getText())){
        Integer horaEntrada = interfaceControladorCarro.getHoraDeEntrada(textPlaca.getText());
        Integer horaSaida = (Integer)horas.getValue()*60 + (Integer)minutos.getValue();
        if(horaSaida < horaEntrada){
            JOptionPane.showMessageDialog(null, " Insira uma hora de saída válida ");
        }
        else if(interfaceControladorCarro.jaPossuiCarro(textPlaca.getText())){
            float valor = calculador.calculaPreco(horaEntrada, horaSaida);
            valorAPagar.setText(Float.toString(valor));
            this.pagar.setEnabled(true);
        }
        }
        else{
            JOptionPane.showMessageDialog(null, "Digite uma placa válida");
        }
    }//GEN-LAST:event_calcularActionPerformed

